    private JLabel infoLabel;

    private NestRepositoryVersionWizardStep wizardStep;

    public NestRepositoryVersionWizardForm(NestRepositoryVersionWizardStep wizardstep) {
        wizardStep = wizardstep;
        NestRepositoryVersionSakerWizardPage wizardpage = wizardstep.getWizardPage();
        String version = ObjectUtils.nullDefault(wizardpage.getVersion(), "");
        versionTextField.getEmptyText().clear().appendText("Version number");

        infoLabel.setText(
                "Default version: " + NestRepositoryClassPathLocation.DEFAULT_VERSION + " (leave text box empty to automatically use it)");

        versionTextField.getDocument().addDocumentListener(new DocumentAdapter() {
            @Override
            protected void textChanged(@NotNull DocumentEvent e) {
                updateWizardStep();
            }
        });
        versionTextField.setText(version);
        updateWizardStep();
    }

    private void updateWizardStep() {
        wizardStep.getWizardPage().setVersion(versionTextField.getText());
        wizardStep.setFormComplete(isFormComplete());
        wizardStep.updateButtons();
    }

    private boolean isFormComplete() {
        String versionstr = versionTextField.getText();
        if (!versionstr.isEmpty() && !NestRepositoryClassPathLocationIDEProperty.isValidVersionNumber(versionstr)) {
            return false;
        }
        return true;
    }

